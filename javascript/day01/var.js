
/**
 * 
 * 선언 -> 컴퓨터에게 기억
 * 변수 (let)
 * 상수 (const)
 * 
 * 
 */

let a;
console.log(a) // undefined 값을 정의하지 않았다

/**
 * 할당 -> 변수/상수에 이 값을 할당할거야
 */

a = 5;

/**
 * 상수 -> 한번 할당된 값을 수정 할 수 없다
 */

const b = 3;
// b = 5(x)재할당

/**
 * 변수 -> 한번 할당한 뒤에도 값을 수정할 수 있다
 */

let c; 
c = 7 // 선언 후 값 할당
let d = 9; //선언과 동시에 값을 할당
d = 10; //재할당 가능

// 변수를 사용하는 이유 ** -> 컴퓨터에게 값을 기억 시켜 불필요한 연산을 하지 않도록 하기 위함


